package OOPS.Polymorphism;

public class SimpleMath {
    int a, b, c;

    SimpleMath(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getC() {
        return c;
    }

    public int add() {
        c = a + b;
        return c;
    }
    public int sub() {
        return a + b;
    }
    public int mul() {
        return a + b;
    }
    public int div() {
        return a + b;
    }
}
